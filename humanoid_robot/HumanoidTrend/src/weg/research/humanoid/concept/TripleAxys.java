package weg.research.humanoid.concept;

public class TripleAxys {
    
    private Integer x;
    private Integer y;
    private Integer z;
    
    public Integer getX() {
        return x;
    }

    public TripleAxys setX(Integer x) {
        this.x = x;
        return this;
    }

    public Integer getY() {
        return y;
    }

    public TripleAxys setY(Integer y) {
        this.y = y;
        return this;
    }

    public Integer getZ() {
        return z;
    }

    public TripleAxys setZ(Integer z) {
        this.z = z;
        return this;
    }
    
}
